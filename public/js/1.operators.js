// = (Assignment )
var x = 10;
var y = x;

//  += (Add and Assign)
x += 5 // x = x + 5
y += x // y = y + x

// -= (Subtract and Assign)
x -= 5 // x = x - 5
y -= x // y = y - x

// *= (Multiply and Assign)
x *= 5 // x = x * 5
y *= x // y = y * x

// /= (Divide and Assign)
x /= 5 // x = x / 5
y /= x // y = y / x

// %= (Remainder and Assign)
x %= 5 // x = x % 5
y %= x // y = y % x

// **= (Exponent and Assign)
x **= 5 // x = x ** 5
y **= x // y = y ** x

// << (Left Shift and Assign)
x <<= 5 // x = x << 5
y <<= x // y = y << x

// >> (Right Shift and Assign)
x >>= 5 // x = x >> 5
y >>= x // y = y >> x

// >>> (Unsigned Right Shift and Assign)
x >>>= 5 // x = x >>> 5

// &= (Bitwise And and Assign)
x &= 5 // x = x & 5
y &= x // y = y & x

// |= (Bitwise Or and Assign)
x |= 5 // x = x | 5
y |= x // y = y | x

// ^= (Bitwise Xor and Assign)
x ^= 5 // x = x ^ 5
y ^= x // y = y ^ x

// <<= (Left Shift Left and Assign)
x <<= 5 // x = x << 5
y <<= x // y = y << x

// >>= (Right Shift Right and Assign)
x >>= 5 // x = x >> 5
y >>= x // y = y >> x

// >>>= (Unsigned Right Shift Right and Assign)
x >>>= 5 // x = x >>> 5
y >>>= x // y = y >>> x
